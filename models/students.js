class Student {
    constructor(name, surname, gender, adress, dateOfBirth, classe, notes ){
        this.name = name;
        this.surname = surname
        this.gender = gender
        this.adress = adress;
        this.dateOfBirth = dateOfBirth;
        this.classe = classe 
        this.notes = notes;
    }
}
module.exports = {Student};